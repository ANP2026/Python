def sample_addition(a, b):
    return a+b
def sample_subtraction(a, b):
    return a-b
def sample_multiplication(a, b):
    return a*b
def sample_divison(a, b):
    return a/b
num1=int(input("Enter the first number: "))
num2=int(input("Enter the second number: "))
print("The sum is =", sample_addition(num1, num2))
print("The difference is =", sample_subtraction(num1, num2))
print("The product is =", sample_multiplication(num1, num2))
print("The division is =", sample_divison(num1, num2))